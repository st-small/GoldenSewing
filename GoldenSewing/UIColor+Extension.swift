import UIKit

extension UIColor {
    struct CustomColors {
        static var burgundy: UIColor  {
            return UIColor(red: 84/255, green: 0, blue: 1/255, alpha: 1)
        }
        static var yellow: UIColor {
            return UIColor(red: 254/255, green: 216/255, blue: 151/255, alpha: 1)
        }
    }
}
